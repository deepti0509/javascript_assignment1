function zeroPositiveNegativeTest(number) {
    if (number == 0) {
        console.log("Number is zero")

    } else if (number < 0) {

        console.log("Number is negative");

    } else {

        console.log("Number is positive")
    }

}

zeroPositiveNegativeTest(0);
zeroPositiveNegativeTest(10);
zeroPositiveNegativeTest(-10);